using System.Windows.Controls;

namespace ReadiMon.Shared.Controls
{
	/// <summary>
	/// ReadiMon Grid View Column class.
	/// </summary>
	/// <seealso cref="GridViewColumn" />
	public class ReadiMonGridViewColumn : GridViewColumn
	{
		/// <summary>
		///     Gets or sets the minimum width.
		/// </summary>
		/// <value>
		///     The minimum width.
		/// </value>
		public double MinWidth
		{
			get;
			set;
		}

		/// <summary>
		///     Gets or sets a value indicating whether [user resized].
		/// </summary>
		/// <value>
		///     <c>true</c> if [user resized]; otherwise, <c>false</c>.
		/// </value>
		public bool UserResized
		{
			get;
			set;
		}

		/// <summary>
		/// Gets or sets a value indicating whether this <see cref="ReadiMonGridViewColumn"/> is hidden.
		/// </summary>
		/// <value>
		///   <c>true</c> if hidden; otherwise, <c>false</c>.
		/// </value>
		public bool Hidden
		{
			get;
			set;
		}
	}
}