namespace ReadiMon.Plugin.Database.Status
{
	/// <summary>
	///     Class representing the SqlServerDatabase type.
	/// </summary>
	public class SqlServerDatabase
	{
		/// <summary>
		///     Initializes a new instance of the <see cref="SqlServerDatabase" /> class.
		/// </summary>
		/// <param name="id">The identifier.</param>
		/// <param name="name">The name.</param>
		/// <param name="fileName">Name of the file.</param>
		/// <param name="size">The size.</param>
		/// <param name="maxPages">The maximum pages.</param>
		/// <param name="growth">The growth.</param>
		public SqlServerDatabase( int id, string name, string fileName, int size, int maxPages, int growth )
		{
			Id = id;
			Name = name;
			FileName = fileName;
			Size = size;
			MaxPages = maxPages;
			Growth = growth;
		}

		/// <summary>
		///     Gets the identifier.
		/// </summary>
		/// <value>
		///     The identifier.
		/// </value>
		public int Id
		{
			get;
			private set;
		}

		/// <summary>
		///     Gets the name.
		/// </summary>
		/// <value>
		///     The name.
		/// </value>
		public string Name
		{
			get;
			private set;
		}

		/// <summary>
		///     Gets the name of the file.
		/// </summary>
		/// <value>
		///     The name of the file.
		/// </value>
		public string FileName
		{
			get;
			private set;
		}

		/// <summary>
		///     Gets the size.
		/// </summary>
		/// <value>
		///     The size.
		/// </value>
		public int Size
		{
			get;
			private set;
		}

		/// <summary>
		///     Gets the maximum pages.
		/// </summary>
		/// <value>
		///     The maximum pages.
		/// </value>
		public int MaxPages
		{
			get;
			private set;
		}

		/// <summary>
		///     Gets the growth.
		/// </summary>
		/// <value>
		///     The growth.
		/// </value>
		public int Growth
		{
			get;
			private set;
		}
	}
}